<script>
    $(function () {
        function active_con() {
            $('.active_back').hide();
            $('.active_con').show();
        }

        function active_back() {
            $('.active_con').hide();
            $('.active_back').show();
        }

        $('.active_tit div').bind('click', function () {
            $('.active_tit div').removeClass('active');
            $(this).addClass('active');
            ($(this).index() == '0') ? active_con() : active_back();
        });
    })
    init();
    function init() {
        var myLatlng = new qq.maps.LatLng(39.922800, 116.389690);
        var myOptions = {
            zoom: 15,
            center: myLatlng,
            mapTypeId: qq.maps.MapTypeId.ROADMAP
        }
        var map = new qq.maps.Map(document.getElementById("container"), myOptions);

        var marker = new qq.maps.Marker({
            position: myLatlng,
            map: map
        });
        var anchor = new qq.maps.Point(18, 49),
            size = new qq.maps.Size(41, 55),
            origin = new qq.maps.Point(0, 0),
            markerIcon = new qq.maps.MarkerImage(
                "/public/img/map_bz.png",
                size,
                origin,
                anchor
            );
        marker.setIcon(markerIcon);
    }
</script>